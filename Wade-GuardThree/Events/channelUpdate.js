const botConfig =  require('../Config/config')
const botChannels = require('../Config/configChannels')

const { safeControl, punish } = require('../Handlers/functionHandler')
const { AuditLogEvent, ChannelType, EmbedBuilder } = require('discord.js')
const client = global.client;

module.exports = {
    name: 'channelUpdate',
    async execute(oldChannel, newChannel) {

        let guild = role.guild;
        let entry = await guild.fetchAuditLogs({ limit: 1, type: AuditLogEvent.ChannelUpdate }).then(audit => audit.entries.first());
        if(!entry || !entry.executor) return;
        let user = member.guild.members.resolve(entry.executor.id);

        const embed = new EmbedBuilder()
        .setColor("Random")
        .setFooter({
            text: `${botConfig.server.footer}`,
            iconURL: client.user?.displayAvatarURL({ dynamic: true })
        })
        .setTimestamp()

        let logChannel = client.channels.cache.get(botChannels.channelLog)

        if(safeControl(member.guild.id, user.id)) {

            if(logChannel) return logChannel.send({ embeds: [embed.setDescription(`${user} adlı yetkili bir kanalı düzenledi, güvenilir listede olduğu için işlem uygulamadım.
                ▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬
                Yetkili: ${user} (\`${user.id}\`)
                Güncellenen Kanal: ${oldChannel.name} (\`${oldChannel.id}\`)
                ▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬
                Tarih: <t:${Math.floor(Date.now() / 1000)}>`)
            ]})
            

        } else {
            await newChannel.permissionOverwrites.set([...oldChannel.permissionOverwrites.cache.values()], { reason: "Wade Channel Guard" });
            punish(user.id, "jail")
            if(logChannel) logChannel.send({ embeds: [embed.setDescription(`${user} adlı yetkili bir kanalı düzenledi, eylemi gerçekleştiren kişiyi sunucudan uzaklaştırdım.
            ▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬
            Yetkili: ${user} (\`${user.id}\`)
            Güncellenen Rol: ${oldChannel.name} (\`${oldChannel.id}\`)
            ▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬
            Tarih: <t:${Math.floor(Date.now() / 1000)}>`)
        ]})
        }

    }
}